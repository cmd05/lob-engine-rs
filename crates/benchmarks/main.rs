use market_events::Side;
use matching_engine::{MatchingEngine, OrderKind, OrderRequest, TimeInForce};
use orderbook::OrderBook;
use replay_engine::ReplayEngine;
use std::env;
use std::error::Error;
use std::fs;
use std::hint::black_box;
use std::path::{Path, PathBuf};
use std::time::{Duration, Instant};

const DEFAULT_MATCHING_OPS: usize = 100_000;
const DEFAULT_LATENCY_OPS: usize = 50_000;
const LATENCY_SAMPLE_LIMIT: usize = 50_000;

#[derive(Debug, Clone)]
struct BenchArgs {
    message_file: PathBuf,
    orderbook_file: PathBuf,
    output_file: Option<PathBuf>,
    matching_ops: usize,
}

#[derive(Debug, Clone)]
struct DurationMetric {
    name: &'static str,
    elapsed: Duration,
    units: usize,
    unit_label: &'static str,
}

#[derive(Debug, Clone)]
struct LatencyMetric {
    name: &'static str,
    samples: Vec<u128>,
}

fn main() {
    if let Err(err) = run() {
        eprintln!("{err}");
        std::process::exit(1);
    }
}

fn run() -> Result<(), Box<dyn Error>> {
    let args = parse_args()?;

    let (parse_metric, events) = time_metric("message_parse", "events", || {
        lobster_parser::parse_message_file(&args.message_file)
    })?;
    let (snapshot_metric, snapshots) = time_metric("orderbook_parse", "snapshots", || {
        validator::parse_orderbook_file(&args.orderbook_file, 10)
    })?;
    let validation_metric = time_void("validated_replay", events.len(), "events", || {
        validator::validate_lobster(&events, &snapshots)
    })?;
    let replay_latency = replay_latency_sample(&events, &snapshots, LATENCY_SAMPLE_LIMIT)?;
    let matching_metric = matching_throughput(args.matching_ops);
    let matching_latency = matching_latency_sample(args.matching_ops.min(DEFAULT_LATENCY_OPS));
    let latency_metric = latency_scheduler_throughput(DEFAULT_LATENCY_OPS);
    let strategies_metric = strategy_suite_throughput(2_000);

    let mut report = String::new();
    report.push_str("# Performance And Optimization Report\n\n");
    report.push_str("Generated by `cargo run --release -p benchmarks -- --output-file docs/PERFORMANCE.md`.\n\n");
    report.push_str("## Dataset\n\n");
    report.push_str(&format!(
        "- Message file: `{}`\n- Orderbook file: `{}`\n- Events: `{}`\n- Visible depth: `10 levels`\n- Validation: `passed`\n\n",
        args.message_file.display(),
        args.orderbook_file.display(),
        events.len()
    ));

    report.push_str("## Throughput\n\n");
    report.push_str("| Benchmark | Work | Time | Throughput |\n");
    report.push_str("| --- | ---: | ---: | ---: |\n");
    for metric in [
        &parse_metric,
        &snapshot_metric,
        &validation_metric,
        &matching_metric,
        &latency_metric,
        &strategies_metric,
    ] {
        report.push_str(&format!(
            "| {} | {} {} | {:.3} s | {} {}/s |\n",
            metric.name,
            metric.units,
            metric.unit_label,
            metric.elapsed.as_secs_f64(),
            format_rate(metric.units, metric.elapsed),
            metric.unit_label
        ));
    }

    report.push_str("\n## Hot-Path Latency\n\n");
    report.push_str("| Hot path | Samples | p50 | p99 |\n");
    report.push_str("| --- | ---: | ---: | ---: |\n");
    for metric in [&replay_latency, &matching_latency] {
        report.push_str(&format!(
            "| {} | {} | {} | {} |\n",
            metric.name,
            metric.samples.len(),
            format_ns(percentile(&metric.samples, 50)),
            format_ns(percentile(&metric.samples, 99))
        ));
    }

    report.push_str("\n## Optimizations Implemented\n\n");
    report.push_str("- Integer tick prices and integer nanosecond timestamps avoid floating-point drift.\n");
    report.push_str("- Parser and orderbook CSV vectors are pre-sized from file metadata to reduce reallocations.\n");
    report.push_str("- Order ID lookup maps directly to `(side, price)` for cancels, deletes, visible executions, and replaces.\n");
    report.push_str("- FOK orders perform a liquidity pre-check before mutating book state.\n");
    report.push_str("- Latency simulation uses deterministic fixed-seed sampling and integer packet-loss parts-per-million.\n");
    report.push_str("- Strategy analytics use const-generic fixed-size metric series for bounded memory behavior.\n");

    report.push_str("\n## Optimization Roadmap\n\n");
    report.push_str("- Replace `BTreeMap<Price, VecDeque<Order>>` hot paths with arena-backed order storage plus intrusive FIFO links.\n");
    report.push_str("- Maintain cached best bid/ask level handles for O(1) best-price reads in synthetic matching.\n");
    report.push_str("- Add a dense tick-array book for bounded-price instruments and compare against the tree book.\n");
    report.push_str("- Add memory-mapped fixed-schema parsing for LOBSTER rows.\n");
    report.push_str("- Add allocation counters and flamegraph captures for replay and matching binaries.\n");
    report.push_str("- Run release benchmarks on a pinned CPU core with turbo/power-state notes for reproducibility.\n");

    if let Some(path) = args.output_file {
        if let Some(parent) = path.parent() {
            if !parent.as_os_str().is_empty() {
                fs::create_dir_all(parent)?;
            }
        }
        fs::write(&path, &report)?;
        println!("Wrote {}", path.display());
    } else {
        print!("{report}");
    }

    Ok(())
}

fn parse_args() -> Result<BenchArgs, Box<dyn Error>> {
    let root = workspace_root();
    let mut message_file = root.join("data/AAPL_2012-06-21_34200000_57600000_message_10.csv");
    let mut orderbook_file = root.join("data/AAPL_2012-06-21_34200000_57600000_orderbook_10.csv");
    let mut output_file = None;
    let mut matching_ops = DEFAULT_MATCHING_OPS;
    let mut args = env::args().skip(1);

    while let Some(arg) = args.next() {
        match arg.as_str() {
            "--message-file" => {
                message_file = PathBuf::from(args.next().ok_or("--message-file requires a path")?);
            }
            "--orderbook-file" => {
                orderbook_file = PathBuf::from(args.next().ok_or("--orderbook-file requires a path")?);
            }
            "--output-file" => {
                output_file = Some(PathBuf::from(args.next().ok_or("--output-file requires a path")?));
            }
            "--matching-ops" => {
                matching_ops = args
                    .next()
                    .ok_or("--matching-ops requires a value")?
                    .parse::<usize>()?;
            }
            "-h" | "--help" => {
                println!(
                    "Usage: cargo run --release -p benchmarks -- [--message-file path] [--orderbook-file path] [--matching-ops n] [--output-file docs/PERFORMANCE.md]"
                );
                std::process::exit(0);
            }
            other => return Err(format!("unknown argument: {other}").into()),
        }
    }

    Ok(BenchArgs {
        message_file,
        orderbook_file,
        output_file,
        matching_ops,
    })
}

fn workspace_root() -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR"))
        .parent()
        .and_then(Path::parent)
        .expect("benchmarks crate should live under crates/")
        .to_path_buf()
}

fn time_metric<T, E>(
    name: &'static str,
    unit_label: &'static str,
    f: impl FnOnce() -> Result<Vec<T>, E>,
) -> Result<(DurationMetric, Vec<T>), E> {
    let started = Instant::now();
    let result = f()?;
    let elapsed = started.elapsed();
    let units = result.len();
    Ok((
        DurationMetric {
            name,
            elapsed,
            units,
            unit_label,
        },
        result,
    ))
}

fn time_void<E>(
    name: &'static str,
    units: usize,
    unit_label: &'static str,
    f: impl FnOnce() -> Result<(), E>,
) -> Result<DurationMetric, E> {
    let started = Instant::now();
    f()?;
    Ok(DurationMetric {
        name,
        elapsed: started.elapsed(),
        units,
        unit_label,
    })
}

fn replay_latency_sample(
    events: &[market_events::MarketEvent],
    snapshots: &[orderbook::Snapshot],
    limit: usize,
) -> Result<LatencyMetric, Box<dyn Error>> {
    let levels = snapshots.first().map(|snapshot| snapshot.asks.len()).unwrap_or(10);
    let mut engine = ReplayEngine::with_book(OrderBook::from_snapshot(&snapshots[0]));
    let mut samples = Vec::with_capacity(limit.min(events.len().saturating_sub(1)));

    for (idx, event) in events.iter().enumerate().skip(1).take(limit) {
        let started = Instant::now();
        engine.apply_lobster_delta(event)?;
        black_box(engine.snapshot(levels));
        samples.push(started.elapsed().as_nanos());
        let actual = engine.snapshot(levels);
        if actual != snapshots[idx] {
            engine = ReplayEngine::with_book(OrderBook::from_snapshot(&snapshots[idx]));
        }
    }

    samples.sort_unstable();
    Ok(LatencyMetric {
        name: "lobster_replay_update_plus_snapshot",
        samples,
    })
}

fn matching_throughput(ops: usize) -> DurationMetric {
    let mut engine = MatchingEngine::new();
    let started = Instant::now();
    for i in 0..ops {
        black_box(engine.process(matching_request(i)));
    }
    DurationMetric {
        name: "synthetic_matching",
        elapsed: started.elapsed(),
        units: ops,
        unit_label: "orders",
    }
}

fn matching_latency_sample(ops: usize) -> LatencyMetric {
    let mut engine = MatchingEngine::new();
    let mut samples = Vec::with_capacity(ops);
    for i in 0..ops {
        let request = matching_request(i);
        let started = Instant::now();
        black_box(engine.process(request));
        samples.push(started.elapsed().as_nanos());
    }
    samples.sort_unstable();
    LatencyMetric {
        name: "synthetic_matching_process",
        samples,
    }
}

fn matching_request(i: usize) -> OrderRequest {
    if i % 2 == 0 {
        OrderRequest::Submit {
            ts: i as u64,
            order_id: 10_000_000 + i as u64,
            side: Side::Sell,
            qty: 1,
            kind: OrderKind::Limit { price: 10_100 },
            tif: TimeInForce::Gtc,
        }
    } else {
        OrderRequest::Submit {
            ts: i as u64,
            order_id: 10_000_000 + i as u64,
            side: Side::Buy,
            qty: 1,
            kind: OrderKind::Limit { price: 10_100 },
            tif: TimeInForce::Ioc,
        }
    }
}

fn latency_scheduler_throughput(ops: usize) -> DurationMetric {
    use latency_sim::{Agent, AgentKind, LatencyModel, LatencySimulator, OutboundOrder};

    let mut sim = LatencySimulator::new(7);
    sim.add_agent(Agent {
        id: 1,
        kind: AgentKind::ColocatedMarketMaker,
        latency: LatencyModel::new(200_000, 100_000, 0),
    });
    sim.add_agent(Agent {
        id: 2,
        kind: AgentKind::RemoteTrader,
        latency: LatencyModel::new(25_000_000, 15_000_000, 0),
    });
    let orders = (0..ops)
        .map(|i| OutboundOrder {
            agent_id: if i % 2 == 0 { 1 } else { 2 },
            send_ts: i as u64,
            request: matching_request(i),
        })
        .collect::<Vec<_>>();

    let started = Instant::now();
    black_box(sim.schedule(&orders));
    DurationMetric {
        name: "latency_scheduler",
        elapsed: started.elapsed(),
        units: ops,
        unit_label: "orders",
    }
}

fn strategy_suite_throughput(runs: usize) -> DurationMetric {
    let started = Instant::now();
    for _ in 0..runs {
        black_box(strategies::run_strategy_suite());
    }
    DurationMetric {
        name: "strategy_suite",
        elapsed: started.elapsed(),
        units: runs,
        unit_label: "runs",
    }
}

fn percentile(sorted_samples: &[u128], percentile: u32) -> u128 {
    if sorted_samples.is_empty() {
        return 0;
    }
    let idx = ((sorted_samples.len() - 1) as u128 * u128::from(percentile) / 100) as usize;
    sorted_samples[idx]
}

fn format_rate(units: usize, elapsed: Duration) -> String {
    if elapsed.is_zero() {
        return "inf".to_string();
    }
    format!("{:.2}", units as f64 / elapsed.as_secs_f64())
}

fn format_ns(ns: u128) -> String {
    if ns >= 1_000_000 {
        format!("{:.3} ms", ns as f64 / 1_000_000.0)
    } else if ns >= 1_000 {
        format!("{:.3} us", ns as f64 / 1_000.0)
    } else {
        format!("{ns} ns")
    }
}
